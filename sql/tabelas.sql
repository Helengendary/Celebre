CREATE DATABASE IF NOT EXISTS Celebre;

CREATE TABLE IF NOT EXISTS Usuario (
	nome VARCHAR(100),
    sobrenome VARCHAR(100),
    email VARCHAR(100),
    celular VARCHAR(100),
    senha VARCHAR(100),
    cep VARCHAR(100),
    cidade VARCHAR(100),
    endereco VARCHAR(100),
    bairro VARCHAR(100),
    numero VARCHAR(100),
    estado VARCHAR(100),
    pais VARCHAR(100),
    genero VARCHAR(100)
);
